package week.one;

public class MaxValues {
    public static void main(String[] args){
        System.out.println("Byte Size is " + Byte.SIZE);
        System.out.println("Byte Max Values is " + Byte.MAX_VALUE);

        System.out.println("Short Size is " + Short.SIZE);
        System.out.println("Short Max Values is " + Short.MAX_VALUE);

        System.out.println("Integer Size is " + Integer.SIZE);
        System.out.println("Integer Max Values is " + Integer.MAX_VALUE);

        System.out.println("Float Size is " + Float.SIZE);
        System.out.println("Float Max Values is " + Float.MAX_VALUE);

        System.out.println("Double Size is " + java.lang.Double.SIZE);
        System.out.println("Double Max Values is " + java.lang.Double.MAX_VALUE);
    }
}
